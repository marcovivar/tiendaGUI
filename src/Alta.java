    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        
        try{
            String nombre=jTextField1.getText();
            double precio=Double.parseDouble(jTextField2.getText());
            int porcentaje=Integer.parseInt(jTextField3.getText());;
            int dia=0;
            int mes=0;
            int agno=0;
            if(jRadioButton1.isSelected()){
                dia=Integer.parseInt(jComboBox1.getSelectedItem().toString());
                mes=Integer.parseInt(jComboBox2.getSelectedItem().toString());
                agno=Integer.parseInt(jComboBox3.getSelectedItem().toString());
                Perecedero per=new Perecedero(nombre,precio,porcentaje,LocalDate.of(agno,mes,dia));
                producto=per;
                home.negocio.altaProducto(producto);
            }else{
                NoPerecedero noPer=new NoPerecedero(nombre,precio,porcentaje);
                producto=noPer;
                home.negocio.altaProducto(producto);
            }

        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "debes llenar los campos correctamente");  
            this.setVisible(false);

        }
        this.setVisible(false);
        
    }//GEN-LAST:event_jButton5ActionPerformed

